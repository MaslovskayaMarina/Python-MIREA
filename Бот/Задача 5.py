from math import *


def main(x, y):
    n = len(x)
    res = 0.0
    for i in range(1, n + 1):
        res += 1 / 46 * \
            exp(27 * x[i - 1] - 2 * y[n - ceil((i) / 3)] ** 2 - 32) \
            ** 4
    return res * 78
